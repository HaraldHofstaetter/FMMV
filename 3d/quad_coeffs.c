void init_quad_coeffs(FmmvHandle *FMMV)
{
	int i;

	if (!FMMV->lambda) { /* no user defined quad coeffs */
		if (FMMV->ws==1) {
			FMMV->lambda = lambda[FMMV->s_eps];
			FMMV->w = w[FMMV->s_eps];
			if (FMMV->reducedScheme) { /* TODO: use other flag */
				FMMV->M = M_gr[FMMV->s_eps];
			}
			else {
				FMMV->M = M[FMMV->s_eps];
			}	
		}
		else { /* ws==2 */
			FMMV->lambda = FMMV_MALLOC(FMMV, FMMV->s_eps*sizeof(_FLOAT1_));
			FMMV->w = FMMV_MALLOC(FMMV, FMMV->s_eps*sizeof(_FLOAT1_));
			FMMV->M = M_ws2[FMMV->s_eps];
			for (i=0; i<FMMV->s_eps; i++)  {
				FMMV->lambda[i] = 0.5*lambda_ws2[FMMV->s_eps][i];
				FMMV->w[i] = 0.5*w_ws2[FMMV->s_eps][i];
			}	
		}	
	}
	
	FMMV->s_exp = 0;
	for (i=0; i<FMMV->s_eps; i++) {
		FMMV->s_exp += FMMV->M[i];
	}
#if (FMM_PRECISION==0)
	/* for single precision s_exp must be divisible by 8 */
	if (FMMV->s_exp%8) {
		for (i=0; i<FMMV->s_exp; i++) {
			if (FMMV->M[i]==4) {
				FMMV->M[i] = 8;
			}
			FMMV->s_exp += 4;
			break;
		}
	}
#endif

}

void finish_quad_coeffs(FmmvHandle *FMMV)
{
	if (FMMV->ws==2) {
		FMMV_FREE(FMMV, FMMV->lambda, FMMV->s_eps*sizeof(_FLOAT1_));
		FMMV_FREE(FMMV, FMMV->w, FMMV->s_eps*sizeof(_FLOAT1_));
	}	
}	
